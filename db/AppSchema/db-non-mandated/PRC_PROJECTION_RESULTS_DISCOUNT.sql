	JOIN #HELPER_TABLE HT ON HT.HELPER_TABLE_SID = CASE @PROGRAM_TYPE
			WHEN 'SCHEDULE CATEGORY'
				THEN RS_CATEGORY
			WHEN 'SCHEDULE TYPE'
				THEN RS_TYPE
			WHEN 'PROGRAM CATEGORY'
				THEN REBATE_PROGRAM_TYPE
			WHEN 'PROGRAM TYPE'
				THEN REBATE_PROGRAM_TYPE
			END
