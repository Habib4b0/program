	UPDATE A
	SET A.SELECTED_LEVEL = CONVERT(VARCHAR(100),B.RS_CONTRACT_SID)
	FROM #MULTISELECT_DISCOUNTS A
	JOIN RS_CONTRACT B ON A.RS_CONTRACT_SID = B.RS_CONTRACT_SID
