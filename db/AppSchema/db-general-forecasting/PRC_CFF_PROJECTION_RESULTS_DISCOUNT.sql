		AND A.RS_CONTRACT_SID = B.MASTER_SID
	JOIN #HELPER_TABLE HT ON HT.HELPER_TABLE_SID = CASE @DISCOUNT_VIEW
			WHEN 'UDC 1'
				THEN UDC1
			WHEN 'UDC 2'
