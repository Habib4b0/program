package com.stpl.gtn.gtn2o.ui.framework.component.splitpanel;

public class GtnUIFrameworkSplitPanelConfig {
	private String splitPosition=null;
	private String minSplitPosition=null;
	private String maxSplitPosition=null;

	public String getSplitPosition() {
		return splitPosition;
	}

	public void setSplitPosition(String splitPosition) {
		this.splitPosition = splitPosition;
	}

	public String getMinSplitPosition() {
		return minSplitPosition;
	}

	public void setMinSplitPosition(String minSplitPosition) {
		this.minSplitPosition = minSplitPosition;
	}

	public String getMaxSplitPosition() {
		return maxSplitPosition;
	}

	public void setMaxSplitPosition(String maxSplitPosition) {
		this.maxSplitPosition = maxSplitPosition;
	}

}
