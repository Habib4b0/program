DECLARE @COUNT INT
	,@ROW INT = 1
	,@NAME VARCHAR(2000)
	,@D_SQL VARCHAR(2000)

SELECT @COUNT = COUNT(DISTINCT ID)
FROM #A

WHILE (@ROW <= @COUNT)
BEGIN
	SET @NAME = (
			SELECT DISTINCT NAME
			FROM #A
			WHERE ID = @ROW
			)
	SET @D_SQL = '

' + @NAME + ''

	exec (@D_SQL)

	SET @ROW = @ROW + 1
END
