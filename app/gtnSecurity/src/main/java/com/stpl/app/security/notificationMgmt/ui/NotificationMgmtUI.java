package com.stpl.app.security.notificationMgmt.ui;


import com.stpl.app.security.notificationMgmt.ui.view.NotificationMgmtView;
import com.vaadin.annotations.AutoGenerated;
import com.vaadin.navigator.Navigator;
import com.vaadin.server.VaadinRequest;
import com.vaadin.server.VaadinSession;
import com.vaadin.ui.UI;

@SuppressWarnings("serial")
// @PreserveOnRefresh
public class NotificationMgmtUI extends UI {

	@AutoGenerated
	Navigator navigator;

	@Override
	protected void init(VaadinRequest request) {
            addStyleName("bootstrap");
            addStyleName("bootstrap-bb");
            String userId = request.getRemoteUser();
            VaadinSession.getCurrent().setAttribute("userId", userId);
		navigator = new Navigator(this, this);
		navigator.addView(NotificationMgmtView.NAME,new NotificationMgmtView());
		

	}
}
