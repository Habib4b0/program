CREATE TRIGGER [DBO].[TRG_ARM_ADJUSTMENT_CONFIG_DEL] 
ON [DBO].[ARM_ADJUSTMENT_CONFIG] 
AFTER DELETE 
AS 
  BEGIN 
  SET NOCOUNT ON
      IF EXISTS (SELECT * 
                 FROM   DELETED) 
        INSERT INTO HIST_ARM_ADJUSTMENT_CONFIG 
                    (ARM_ADJUSTMENT_CONFIG_SID, 
                     TRANSACTION_NAME, 
                     TRANSACTION_DESC, 
                     METHODOLGY, 
                     REDEMPTION_PERIOD, 
                     CREATED_DATE, 
                     CREATED_BY, 
                     MODIFIED_DATE, 
                     MODIFIED_BY, 
                     ACTION_FLAG) 
        SELECT ARM_ADJUSTMENT_CONFIG_SID, 
               TRANSACTION_NAME, 
               TRANSACTION_DESC, 
               METHODOLGY, 
               REDEMPTION_PERIOD, 
               CREATED_DATE, 
               CREATED_BY, 
               MODIFIED_DATE, 
               MODIFIED_BY, 
               'D' 
        FROM   DELETED 
  END 

