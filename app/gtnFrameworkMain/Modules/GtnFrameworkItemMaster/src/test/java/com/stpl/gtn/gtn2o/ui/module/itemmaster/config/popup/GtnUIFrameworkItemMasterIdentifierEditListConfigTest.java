     public void testComponentConfig(String gtnUIFrameworkItemMasterIdentifierEditListConfigMethodName, GtnUIFrameworkItemMasterIdentifierEditListConfig gtnUIFrameworkItemMasterIdentifierEditListConfig) {
        System.out.println(gtnUIFrameworkItemMasterIdentifierEditListConfigMethodName);
        Method arr[] = gtnUIFrameworkItemMasterIdentifierEditListConfig.getClass().getDeclaredMethods();
        Method method = null;
        for (Method met : arr) {
            if (gtnUIFrameworkItemMasterIdentifierEditListConfigMethodName.equals(met.getName())) {
                method = met;
                break;
            }
        }
        int methodParams = method != null ? method.getParameterCount() : 0;
        if (methodParams != 0) {
            List<GtnUIFrameworkComponentConfig> componentList = getComponentList(method, gtnUIFrameworkItemMasterIdentifierEditListConfig);
            assertFalse(componentList.isEmpty());
        }
    }

    private List<GtnUIFrameworkComponentConfig> getComponentList(Method method, GtnUIFrameworkItemMasterIdentifierEditListConfig gtnFrameworkItemMastertMainConfig) throws SecurityException {
        List<GtnUIFrameworkComponentConfig> componentList = new ArrayList<>();
        try {
            Class[] classArr = method.getParameterTypes();
            Object[] obj = new Object[method.getParameterTypes().length];
            obj[0] = componentList;
            for (int i = 1; i < obj.length; i++) {
                String name = classArr[i].getName();
                switch (classArr[i].getName()) {
                    case "java.lang.String":
                        obj[i] = "VALUE";
                        break;
                    case "java.util.List":
                        obj[i] = new ArrayList<>();
                        break;
                    case "com.stpl.gtn.gtn2o.config.GtnFrameworkComponentConfigProvider":
                        obj[i] = GtnFrameworkComponentConfigProvider.getInstance();
                        break;
                    default:
                        break;
                }
            }
            method.setAccessible(true);
            method.invoke(gtnFrameworkItemMastertMainConfig, obj);
        } catch (IllegalAccessException | IllegalArgumentException | InvocationTargetException ex) {
            Logger.getLogger(GtnUIFrameworkItemMasterIdentifierEditListConfigTest.class.getName()).log(Level.SEVERE, null, ex);
        }
        return componentList;
    }
    
